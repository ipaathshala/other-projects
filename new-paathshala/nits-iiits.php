                  <div class="card-block padding-30px">
                    <p class="text-justify"><i class="fa fa-globe"></i> The Bachelor of Technology (BTech) degree is the most common undergraduate degree in the NITs in terms of student enrollment. The BTech course is based on a 4-year program with eight semesters, while the Dual Degree and Integrated courses are 5-year programs with ten semesters. In all NITs, the first year of BTech and Dual Degree courses are marked by a common course structure for all the students, though in some NITs, a single department introduction related course is also included. The common courses include the basics from most of the departments like Electronics, Mechanics, Chemistry, Electrical and Physics. At the end of first year, some NITs offer an option to the meritorious students to change departments on the basis of their performance in the first two semesters. Few such changes ultimately take place as the criteria for them are usually strict, limited to the most meritorious students. Few NITs also offer 5-year Bachelor of Architecture (BArch) and 4-year Bachelor of Science (BSc) degrees.</p>
                    <p class="text-justify"><i class="fa fa-globe"></i> From the second year onwards, the students study subjects exclusively from their respective departments. In addition to these, the students have to take compulsory advanced courses from other departments in order to broaden their education. Separate compulsory courses from humanities and social sciences department, and sometimes management courses are also enforced. In the last year of their studies, most of the students are placed into industries and organisations via the placement process of the respective NIT, though some students opt out of this either when going for higher studies or when they take up jobs by applying to the companies directly.</p>
                  </div>
                </div>
              </div>
              <div class="card">
                <div class="card-header" role="tab" id="headingOne">
                  <h5 class="mb-0">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse-5" aria-expanded="true" aria-controls="collapseOne" class="d-block text-dark text-capitalize text-up-small font-weight-700">Indian Institutes of Information Technology [IIIT]</a>
                  </h5>
                </div>
                <div id="collapse-5" class="collapse hide" role="tabpanel" aria-labelledby="headingOne">
                  <div class="card-block padding-30px">
                    <p class="text-justify">Indian Institutes of Information Technology (IIITs) are a group of institutes of higher education in India, focused on information technology. Five of them are established, funded and managed by the Ministry of Human Resource Development. The rest are set up on the public-private partnership (PPP) model.</p>
                    <p class="text-justify">Prior to 2010, the IIITs at Gwalior, Allahabad, Jabalpur and Kancheepuram were set up by the Ministry of Human Resource Development (MHRD).</p>
                    <p class="text-justify">In 2010 the Indian Institutes of Information Technology Bill 2010 was introduced in Lok Sabha.[citation needed] The bill sought to grants the status of Institutes of National Importance (INI) upon the four IIITs. After much debate and changes, the bill was passed by the Parliament on December 1, 2014[citation needed] and published as the Indian Institutes of Information Technology Act, 2014 on 8 December 2014, coming into effect on 5 January 2015.</p>
                    <p class="text-justify">In 2015, a fifth institute was set up by the MHRD in Kurnool,[1] as part as the government obligation under the Andhra Pradesh Reorganisation Act, 2014. The Indian Institutes of Information Technology (Amendment) Act, 2017 granted the INI status to the Kurnool institute.</p>
                    <p class="text-justify">Besides the above fully government funded IIITs, in 2010 the Union Cabinet approved a scheme for setting up twenty more IIITs, based on the public-private partnership (PPP) model, funded by the central government, state governments and industry partners in the ratio 50:35:15. As of 2014, 23 proposals were made by 21 states (Andhra Pradesh (Chittoor & Kakinada), Assam, Bihar, Chhattisgarh, Goa, Gujarat, Haryana, Himachal Pradesh, Jharkhand, Karnataka, Kerala, Madhya Pradesh, Maharashtra (Pune & Nagpur), Manipur, Orissa, Punjab, Rajasthan, Tamil Nadu, Tripura, West Bengal and Uttar Pradesh), and 16 proposals were approved (Andhra Pradesh (Chittoor), Andhra Pradesh (Kakinada), Assam, Gujarat, Haryana, Himachal Pradesh, Karnataka, Kerala, Madhya Pradesh, Rajasthan, Tamil Nadu , Maharashtra (Pune), Uttar Pradesh, West Bengal , Manipur and Tripura). As of 2017 14 of these IIITs were set up, the ones not being set up are the ones in Kakinada, Andhra Pradesh and in Bodhjungnagar, Tripura. In addition, in 2015 the MHRD approved an IIIT in Nagpur, Maharashtra and in Ranchi, Jharkhand. An IIIT in Bhagalpur, Bihar was approved in 2016. Finally, to close the list of 20, an IIIT in Surat, Gujarat was approved and set up in 2017. As of September 2017, these four institutes were already established, leaving only the institutes in Kakinada and Bodhjungnagar which are yet to be established.</p>
                    <p class="text-justify">On 9th August 2017, The Indian Institutes of Information Technology (Public-Private Partnership) Act, 2017 has been notified in the Gazette of India. The Act confers the status of Institutes of National Importance to fifteen IIITs set up on the public-private partnership (PPP) mode in Vadodara, Guwahati, Sri City, Kota, Tiruchirapalli, Kalyani, Una, Sonepat, Lucknow, Kottayam, Senapati (Manipur), Dharwad, Pune, Nagpur and Ranchi.</p>
                    <p class="text-justify">The three IIITs set up under the PPP model in 2017, namely institutes in Surat, Bhopal and Bhagalpur, were not mentioned in the 2017 PPP act, nor were the institutes in Kakinada and Bodhjungnagar.</p>
                    <p class="text-justify">In 2018, the Government of Tripura approved ₹50.67 crore (US$7.1 million) for the establishment of IIIT Bodhjungnagar, which was stalled since 2012, covering ₹5.82 crore (US$810,000) which were missing from private sources under the PPP model. The institution was set to start classes in the 2018-2019 academic session from the NIT Agartala campus.</p>
