                  <h5 class="mb-0">
                    <a data-toggle="collapse" data-parent="#accordion" href="#collapse-3" aria-expanded="true" aria-controls="collapseOne" class="d-block text-dark text-capitalize text-up-small font-weight-700">Education at NITs</a>
                  </h5>
                </div>
                <div id="collapse-3" class="collapse hide" role="tabpanel" aria-labelledby="headingOne">
                  <div class="card-block padding-30px">
                    <p class="text-justify"> The NITs along with the IITs receive comparatively higher grants than other engineering colleges in India. Average NIT funding increased to ₹100 crores ($15.4 million) by year 2011. On average, each NIT also receives ₹ 20-25 crore ($3-3.8 million) under World Bank funded Technical Education Quality Improvement Program (TEQIP I and TEQIP II). Other sources of funds include student fees and research funding from industry and contributions from the alumni. The faculty-to-student ratio in the NITs is between 1:7 and 1:9. The cost borne by undergraduate students is around ₹ 125,000 ($1934) per annum. After students from SC and ST categories, physically challenged students will now be the beneficiaries of fee waiver at the NITs in India.</p>
                    <p class="text-justify"> The various NITs function autonomously, and their special status as Institutes of National Importance facilitates the smooth running of NITs, virtually free from both regional as well as student politics. Such autonomy means that NITs can create their own curricula and adapt rapidly to the changes in educational requirements, free from bureaucratic hurdles. The medium of instruction in all NITs is English. The classes are usually held between 8:30 am and 5:30 pm, though there are some variations within each NIT. All the NITs have public libraries for the use of their students. In addition to a collection of prescribed books, the libraries have sections for fiction and other literary genres. Electronic libraries allow students access to online journals and other periodicals through the AICTE-INDEST consortium, an initiative by the Ministry of Human Resource Development. Students also have access to IEEE documents and journals.</p>
                    <p class="text-justify"> The academic policies of each NIT are decided by its Senate. This comprises all professors of the NIT and student representatives. Unlike many western universities that have an elected senate, the NITs have an academic senate. It controls and approves the curriculum, courses, examinations and results, and appoints committees to look into specific academic matters. The teaching, training and research activities of the institute are periodically reviewed by the senate to maintain educational standards. The Director of NIT is the ex-officio Chairman of the Senate.</p>
                    <p class="text-justify"> Stringent faculty recruitment and industry collaboration also contribute to NIT success. Faculty other than lecturers must have a Ph.D. and relevant teaching and industry experience. Existing faculty who do not meet these criteria enroll under a Quality Improvement Programme (QIP) at IITs and IISc.
                    </p>
                    <p class="text-justify"> All the NITs follow the credits system of performance evaluation, with proportional weighting of courses based on their importance. The total marks (usually out of 100) form the basis of grades, with a grade value (out of 10) assigned to a range of marks. Sometimes, relative grading is done considering the overall performance of the whole class. For each semester, the students are graded on a scale of 0 to 10 based on their performance, by taking a weighted average of the grade points from all the courses, with their respective credit points. Each semester evaluation is done independently and then the weighted average over all semesters is used to calculate the cumulative grade point average (CGPA).
                    </p>
