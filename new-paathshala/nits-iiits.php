                <div id="collapse-1" class="collapse" role="tabpanel" aria-labelledby="headingOne">
                  <div class="card-block padding-30px">
                    <p class="text-justify">The National Institutes of Technology (NITs) are autonomous public institutes of higher education, located in India. They are governed by the National Institutes of Technology Act, 2007, which declared them as institutions of national importance alongside Indian Institutes of Technology. These institutes of national importance receive special recognition from the Government of India. The NIT Council is the supreme governing body of India's National Institutes of Technology (NIT) system and all 31 NITs are funded by the Government of India. These institutes are among the top ranked engineering colleges in India and have one of the lowest acceptance rates for engineering institutes, of around 2 to 3 percent, second only to the Indian Institutes of Technology (IITs) in India. All NITs are autonomous which enables them to set up their own curriculum. The language of instruction is English at all these institutes.</p>
                    <p class="text-justify">NITs offer degree courses at bachelors, masters, and doctorate levels in various branches of engineering, architecture, management and science. Admission to the under-graduate courses such as Bachelor of Technology (B.Tech.) and Bachelor of Architecture (B.Arch) programs in NITs are through the highly competitive Joint Entrance Examination (Main). Admission to postgraduate courses are through the Graduate Aptitude Test in Engineering for Master of Technology (M.Tech.) and Master of Science (M.Sc.) programs, Common Admission Test for Master of Business Administration (MBA) program and NIMCET for Master of Computer Applications (MCA) program.</p>
                    <p class="text-justify">Since 2015, the Joint Seat Allocation Authority and Centralized Counselling for M.Tech/M.Arch and M.Plan conduct the admission process for undergraduate and postgraduate programs respectively in all NITs. As of 2017, the total number of seats for undergraduate programs is 19,000 and for post graduate programs is 8,050 in all 31 NITs.</p>
