                      <div class="card-block padding-30px">
                        <p class="text-justify">The Kishore Vaigyanik Protsahan Yojana (KVPY) is an on-going National Program of Fellowship in Basic Sciences, initiated and funded by the Department of Science and Technology, Government of India, to attract exceptionally highly motivated students for pursuing basic science courses and research career in science.</p>
                        <p class="text-justify">The objective of the program is to identify students with talent and aptitude for research; help them realize their academic potential; encourage them to take up research careers in Science, and ensure the growth of the best scientific minds for research and development in the country.</p>
                        <p class="text-justify">The advertisement for the KVPY Fellowship appears in all the national dailies normally on the Technology Day (May 11) and the Second Sunday of July every year.</p>
                        <p class="text-justify">Selection of the students is made from those studying in XI standard to 1st year of any undergraduate Program in Basic Sciences namely B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./M.S. in Mathematics, Physics, Chemistry and Biology having aptitude for scientific research. Special groups / Committees are set up at IISc to screen the applications and conduct an aptitude test at various centres in the country. Based on the performance in the aptitude test, short-listed students are called for an interview which is the final stage of the selection procedure. For receiving a fellowship, both aptitude test and interview marks are considered.</p>
                        <p class="text-justify">Generous fellowships are provided up to the pre-Ph.D. level to the selected KVPY Fellows.</p>
