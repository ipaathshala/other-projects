                        <p class="text-justify">Stream SA: Students enrolled in XI Standard (Science Subjects) during the academic year 2018-19 and having secured a minimum of 75% (65% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects in the X Standard Board examination. The fellowship of the students selected under this stream will be activated only if they join an undergraduate course in Basic Sciences (B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./Int. M.S.) in the academic year 2020-21 after having secured a minimum of  60% (50% for SC/ST/PWD) marks in aggregate in Science subjects in the XII standard/(+2) Board Examination. During the interim period of one year they will be invited for the National  Science (Vijyoshi) Camp and their travel expenses and local hospitality will be met by KVPY.</p>
                        <p class="text-justify">Stream SX: Students enrolled in XII Standard/ (+2) (Science subjects) during the academic year 2018–19 and aspiring to join undergraduate program in Basic Sciences namely Physics/Chemistry/Mathematics & Biology leading to  B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./Int. M.S. for the session 2019–20 provided they have secured a minimum of 75% (65% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/Chemistry/Biology) in the X Standard Board Examination and a minimum of  60% (50% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/Chemistry/Biology) in the XII standard Board Examination.</p>
                        <p class="text-justify">Students in their 2nd year of study in Cambridge International Examination Board and aspiring to join an UG program (viz. B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./Int. M.S.)in Basic Sciences namely Physics, Chemistry, Mathematics and Biology in the Academic year 2018-19 are eligible to apply provided they have secured a minimum of 75% (65% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/Chemistry/Biology) in the X Standard Board Examination. They must secure 60% (50% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/Chemistry/Biology) in the XII standard Board Examination before taking up the fellowship, if awarded.</p>
                        <p class="text-justify">Stream SB: Students enrolled in the 1st year of undergraduate program in Basic Sciences namely Physics/Chemistry/Mathematics & Biology leading to B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./Int. M.S. during the academic year 2018–19 and having secured a minimum of 60% (50% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/Chemistry/Biology)  in the XII Standard Board Examination. In the 1st year final examination of B.Sc./B.S./B.Math./B.Stat./Int. M.Sc./Int. M.S. they must secure 60% (50% for SC/ST/PWD) marks before taking up the fellowship, if awarded.</p>
                        <p class="text-justify">Empowerment initiative in the KVPY Fellowship Program:</p>
                        <p class="text-justify">A certain number of additional fellowships exclusively for the students belonging to SC/ST community under the various streams as stated above will be operated. A certain number of fellowships under various streams as stated above will be operated exclusively for students under the category of Persons With Disability (Physically and Visually Challenged). Students enrolled in an undergraduate course in Basic Sciences, that is, B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./Int. M.S. in Chemistry, Physics, Mathematics, Statistics, Biochemistry, Microbiology, Cell Biology, Ecology, Molecular Biology, Botany, Zoology, Physiology, Biotechnology, Neurosciences, Bioinformatics, Marine Biology, Geology, Human Biology, Genetics, Biomedical Sciences, Applied Physics, Geophysics, Materials Science or Environmental Science are eligible for KVPY fellowship.</p>
